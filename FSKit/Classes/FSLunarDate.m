#import "FSLunarDate.h"

@implementation FSLunarDate

+ (FSLunarDate *)lunarWithYear:(NSInteger)year month:(NSInteger)month day:(NSInteger)day {
    return [self lunarWithYear: year month: month day: day hour: 0 minute: 0 second: 0];
}

+ (FSLunarDate *)lunarWithYear:(NSInteger)year month:(NSInteger)month day:(NSInteger)day hour:(NSInteger)hour minute:(NSInteger)minute second:(NSInteger)second {
    FSLunarDate *lunar = [[FSLunarDate alloc] init];
    lunar.year = year;
    lunar.month = month;
    lunar.day = day;
    lunar.hour = hour;
    lunar.minute = minute;
    lunar.second = second;
    return lunar;
}

@end
